<script type="text/javascript">
	$(document).ready(function(){
		$(".first.twd-content").addClass("tabImage");
		$(".twd-tab").on("click", function(){
			$(".twd-tab").removeClass("twd-tab-active");
			$(this).addClass("twd-tab-active");
			var id = $(this).attr("id");
			$(".twd-content").hide();
			$(".tab-img").removeClass("show");
			$(this).addClass("show");
			
			$("." + id).fadeIn(1000);
			$("." + id).addClass("tabImage");
		});
		
		if ($('.hero-header').length) {
			var stickySidebar = $('.hero-header').offset().top;
		}
		$(window).scroll(function() {  
			if ($(window).scrollTop() > stickySidebar) {
				$('.hero-header').addClass('affix');
			}
			else {
				$('.hero-header').removeClass('affix');
			}  
		});

	});	
</script>